#!/usr/bin/env python3
"""Build a workflow.scripps.edu-importable workflow zip from a Python recipe.

A workflow zip looks like::

    <workflow-name>.zip
    +-- <workflow-name>.json          # graph: connections + content + metadata
    +-- NODE_<process>_<hex>.zip      # one bundle per Process node referenced
    +-- NODE_<widget>_<hex>.zip       # one bundle per Input widget
    +-- NODE_tag_<key>_<hex>.zip      # one bundle per inserted tag node

This module exposes the :class:`Workflow` DSL plus :func:`Workflow.export`
which materializes the whole tree on disk + zips it. Recipes live under
``workflows/`` (see ``workflows/nmr_predictor.py`` for a worked example).

Design notes / conventions
==========================

* **Process nodes** come from :data:`tools.export_nodes.NODES` — they
  reference the live ``scripps_workflow.nodes.*`` implementations via
  the same shim (``script.sh`` + ``script.py``) the node exporter
  produces. To add a new process node to a workflow, register it in
  ``NODES`` first.

* **Widgets** ("Number", "Combo box", "Check box", "Text") are pure
  GUI sources — no script, just a single output socket. They are
  declared with :meth:`Workflow.widget`.

* **Tag nodes** are auto-inserted whenever a widget feeds into a Process
  node's *named* config input (i.e. anything other than the upstream
  pointer). Each tag instance bundles a tiny per-instance ``tag_input.py``
  with ``KEY = "<config_key>"`` baked in (matching the GUI's existing
  per-instance convention so the engine can run them with no extra
  install). The constant edit pattern is exactly what the imported
  example used.

* **Pointer connections** are declared with :meth:`Workflow.pointer`.
  The DSL forces the destination input to be ``"pointer JSON"``, since
  that's the canonical name we baked into ``tools/export_nodes.py``.

* **node_id** values are sha1-derived from each node's *display name*
  in the workflow (Process node names come from their ``wf-*``
  identifier; widgets and tag instances use the alias / key the recipe
  picked). This keeps IDs internally consistent across the per-node
  bundles and the workflow's content/connection records — the engine's
  importer is expected to remap on the way in, but they must resolve
  inside the bundle.

Stdlib only.
"""
